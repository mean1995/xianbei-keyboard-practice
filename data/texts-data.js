function createTextEntries(groupId, groupTitle, groupDescription, titlePrefix, description, lineSets, firstId) {
  return lineSets.map((lines, index) => {
    const number = String(index + 1).padStart(2, "0");

    return {
      id: index === 0 ? firstId : `${groupId}-${number}`,
      groupId,
      groupTitle,
      groupDescription,
      title: `${titlePrefix} ${number}`,
      description,
      lines
    };
  });
}

